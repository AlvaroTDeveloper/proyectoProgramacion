package proyectoProgramacion.zBasura;

public class PeliUsu {

	private int ideusu;
	private int idepeli;

	public PeliUsu(int ideusu, int idepeli) {
		this.ideusu = ideusu;
		this.idepeli = idepeli;
	}

	public int getIdeusu() {
		return ideusu;
	}

	public void setIdeusu(int ideusu) {
		this.ideusu = ideusu;
	}

	public int getIdepeli() {
		return idepeli;
	}

	public void setIdepeli(int idepeli) {
		this.idepeli = idepeli;
	}

}
