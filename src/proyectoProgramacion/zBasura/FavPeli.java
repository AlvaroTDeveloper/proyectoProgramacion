package proyectoProgramacion.zBasura;

public class FavPeli {

	private int idepeli;
	private int idelist;

	public FavPeli(int idepeli, int idelist) {
		this.idepeli = idepeli;
		this.idelist = idelist;
	}

	public int getIdepeli() {
		return idepeli;
	}

	public void setIdepeli(int idepeli) {
		this.idepeli = idepeli;
	}

	public int getIdelist() {
		return idelist;
	}

	public void setIdelist(int idelist) {
		this.idelist = idelist;
	}

}
